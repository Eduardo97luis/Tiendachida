    public String usu(){
        ResultSet datos = conexion.consultas("select * from portillito.usuarioactivo");
        String nombre="";
        try {
            while(datos.next()){
                nombre=datos.getString("nombre");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        return nombre;
    }
